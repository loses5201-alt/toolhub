/*
  JSON → Go struct 產生引擎的回歸測試(node 直接跑)。
  執行:node scripts/test-jsontogo.mjs
*/
import { build } from 'esbuild'
import { tmpdir } from 'node:os'
import { join } from 'node:path'

const out = join(tmpdir(), `jsontogo-test-${Date.now()}.mjs`)
await build({
  entryPoints: ['src/features/jsonToGo.ts'],
  bundle: true,
  format: 'esm',
  outfile: out,
  logLevel: 'silent',
})
const { goFieldName, goType, generateGo } = await import('file://' + out)

let fail = 0
function eq(note, got, want) {
  if (got === want) console.log(`✓ ${note}`)
  else {
    fail++
    console.error(`✗ ${note}\n   got :\n${got}\n   want:\n${want}`)
  }
}

// --- goFieldName ---
eq('id → ID', goFieldName('id'), 'ID')
eq('userId → UserID', goFieldName('userId'), 'UserID')
eq('user_name → UserName', goFieldName('user_name'), 'UserName')
eq('html-url → HTMLURL', goFieldName('html-url'), 'HTMLURL')
eq('first.name → FirstName', goFieldName('first.name'), 'FirstName')
eq('api_key → APIKey', goFieldName('api_key'), 'APIKey')
eq('數字開頭加底線', goFieldName('1st'), '_1st')
eq('全符號 → _', goFieldName('***'), '_')
eq('JSONData 保留', goFieldName('JSONData'), 'JSONData')

// --- goType 純量 ---
eq('bool', goType(true), 'bool')
eq('int', goType(3), 'int')
eq('float64', goType(3.5), 'float64')
eq('string', goType('x'), 'string')
eq('null → interface{}', goType(null), 'interface{}')
eq('空陣列', goType([]), '[]interface{}')
eq('int 陣列', goType([1, 2, 3]), '[]int')
eq('string 陣列', goType(['a']), '[]string')
eq('int/float 混合陣列 → []float64', goType([1, 2.5]), '[]float64')
eq('混型別陣列 → []interface{}', goType([1, 'a']), '[]interface{}')

// --- 簡單物件 ---
eq(
  '簡單物件',
  generateGo('{"id":1,"name":"x"}', 'User').code,
  'type User struct {\n\tID int `json:"id"`\n\tName string `json:"name"`\n}',
)

// --- 巢狀物件(內嵌 struct)---
const nested = generateGo('{"profile":{"age":20}}', 'Root').code
eq(
  '巢狀內嵌 struct',
  nested,
  'type Root struct {\n\tProfile struct {\n\t\tAge int `json:"age"`\n\t} `json:"profile"`\n}',
)

// --- 物件陣列合併欄位(聯集)---
const arrObj = generateGo('{"items":[{"a":1},{"a":2,"b":"x"}]}', 'Root').code
eq(
  '物件陣列欄位聯集',
  arrObj,
  'type Root struct {\n\tItems []struct {\n\t\tA int `json:"a"`\n\t\tB string `json:"b"`\n\t} `json:"items"`\n}',
)

// --- 陣列元素型別衝突 → interface{} ---
const conflict = generateGo('{"items":[{"a":1},{"a":"x"}]}', 'Root').code
eq(
  '欄位型別衝突退 interface{}',
  conflict,
  'type Root struct {\n\tItems []struct {\n\t\tA interface{} `json:"a"`\n\t} `json:"items"`\n}',
)

// --- 頂層陣列 ---
eq('頂層物件陣列', generateGo('[{"x":1}]', 'Root').code, 'type Root []struct {\n\tX int `json:"x"`\n}')
eq('頂層純量', generateGo('42', 'Root').code, 'type Root int')

// --- null 與 缺欄位 ---
eq('null 欄位', generateGo('{"v":null}', 'Root').code, 'type Root struct {\n\tV interface{} `json:"v"`\n}')

// --- 解析錯誤 ---
const bad = generateGo('{not json}', 'Root')
eq('解析失敗 ok=false', bad.ok, false)
eq('解析失敗有 error', typeof bad.error === 'string' && bad.error.length > 0, true)

// --- rootName 預設與正規化 ---
eq('rootName 預設', generateGo('1').code, 'type AutoGenerated int')
eq('rootName 正規化', generateGo('1', 'my type').code, 'type MyType int')

if (fail) {
  console.error(`\n${fail} 個測試未通過`)
  process.exit(1)
} else {
  console.log('\n全部 jsonToGo 測試通過')
}
