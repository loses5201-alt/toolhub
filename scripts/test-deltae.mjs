// ── 2) 已知 sRGB → Lab(D65)──
function labApprox(name, rgb, L, a, b, tol = 0.02) {
  const lab = rgbToLab(rgb)
  approx(`${name} L`, lab.L, L, tol)
  approx(`${name} a`, lab.a, a, tol)
  approx(`${name} b`, lab.b, b, tol)
}
labApprox('白 #fff', { r: 255, g: 255, b: 255 }, 100, 0, 0)
labApprox('黑 #000', { r: 0, g: 0, b: 0 }, 0, 0, 0)
labApprox('中灰 #808080', { r: 128, g: 128, b: 128 }, 53.5851, 0, 0)
labApprox('紅 #f00', { r: 255, g: 0, b: 0 }, 53.2408, 80.0925, 67.2032)
labApprox('綠 #0f0', { r: 0, g: 255, b: 0 }, 87.7347, -86.1827, 83.1793)
labApprox('藍 #00f', { r: 0, g: 0, b: 255 }, 32.297, 79.1875, -107.8602)

// ── 3) ΔE76 / 自洽性 ──
approx('ΔE76 同色=0', deltaE76({ L: 50, a: 2, b: 3 }, { L: 50, a: 2, b: 3 }), 0, 1e-9)
approx('ΔE76 純 L 差', deltaE76({ L: 40, a: 0, b: 0 }, { L: 50, a: 0, b: 0 }), 10, 1e-9)
approx('ΔE76 3-4-5', deltaE76({ L: 0, a: 0, b: 0 }, { L: 0, a: 3, b: 4 }), 5, 1e-9)
approx('ΔE2000 同色=0', deltaE2000({ L: 50, a: 20, b: -30 }, { L: 50, a: 20, b: -30 }), 0, 1e-9)
